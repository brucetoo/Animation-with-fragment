package com.bruce.animationwithfragment;

import java.util.Arrays;

public class Positions {
	private float mFocusY;
	private float[] mNextContainersY;
	private float mNextContainersAlpha;
	private float mStickToY;
	private float mEditY;
	private float mEditAlpha;
	private float[] mSlideToTop;

	public Positions(float focusY, float[] nextContainersY, float nextContainersAlpha, float stickToY, float editY, float editAlpha, float[] slideToTop) {
		mFocusY = focusY;
		mNextContainersY = Arrays.copyOf(nextContainersY, nextContainersY.length);
		mNextContainersAlpha = nextContainersAlpha;
		mStickToY = stickToY;
		mEditY = editY;
		mEditAlpha = editAlpha;
		mSlideToTop = Arrays.copyOf(slideToTop, slideToTop.length);
	}

	public float getFocusY() {
		return mFocusY;
	}

	public float[] getNextContainersY() {
		return mNextContainersY;
	}

	public float getNextContainersAlpha() {
		return mNextContainersAlpha;
	}

	public float getStickToY() {
		return mStickToY;
	}

	public float getEditY() {
		return mEditY;
	}

	public float getEditAlpha() {
		return mEditAlpha;
	}

	public float[] getSlideToTop() {
		return mSlideToTop;
	}
}
